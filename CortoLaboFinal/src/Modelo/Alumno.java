package Modelo;

/**
 *
 * @author LN710Q
 */
public class Alumno {
    String carnet,nombres,apellidos,universidad;
    int id, edad,estado;

    public Alumno(String carnet, String nombres, String apellidos, String universidad, int id, int edad, int estado) {
        this.carnet = carnet;
        this.nombres = nombres;
        this.apellidos = apellidos;
        this.universidad = universidad;
        this.id = id;
        this.edad = edad;
        this.estado = estado;
    }

    public Alumno() {
    }
    

    public String getCarnet() {
        return carnet;
    }

    public void setCarnet(String carnet) {
        this.carnet = carnet;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public String getApellidos() {
        return apellidos;
    }

    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    public String getUniversidad() {
        return universidad;
    }

    public void setUniversidad(String universidad) {
        this.universidad = universidad;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public int getEstado() {
        return estado;
    }

    public void setEstado(int estado) {
        this.estado = estado;
    }
    
    
}
